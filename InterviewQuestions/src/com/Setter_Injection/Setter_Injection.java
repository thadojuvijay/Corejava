package com.Setter_Injection;

public class Setter_Injection 
{
private Integer eno;
private String name;


public void setEno(Integer eno)
{
	this.eno=eno;
}

public Integer getEno()
{
	return eno;
}

public void setName(String name)
{
	this.name=name;
}

public String getName()
{
	return name;
}

}
