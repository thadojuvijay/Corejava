package com.ArithmeticOperator;

public class RelationalOperator 
{
public static void main(String[] args) 
{
	int a=1;
	int b=2;
	
	
	if(a>b)
	{
	System.out.println("true");	
	}
	else
	{
		System.out.println("yes");
	}
	
	
	if(a<b)
	{
		System.out.println("yes");
	}
	else
	{
		System.out.println("no");
	}
	
	if(a==b){
		System.out.println("true");
	}
	else
	{
		System.out.println("false");
	}
	

}
}
